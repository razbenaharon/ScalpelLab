    def __init__(self, master: tk.Tk, on_load_callback):
        self.master = master
        self.on_load_callback = on_load_callback
        
        # Load config
        self.db_path, self.mpv_path = self._get_config_values()
        
        # State
        self.cases: List[Case] = []
        self.filtered_cases: List[Case] = []
        self.thumb_cache = {} # Map item_id -> PhotoImage
