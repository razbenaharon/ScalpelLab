    def _get_config_values(self):
        config_ini = configparser.ConfigParser()
        # Config is at MPV_DB root, go up one level from lib/
        base_dir = os.path.dirname(os.path.dirname(os.path.abspath(__file__)))
        config_path = os.path.join(base_dir, 'config.ini')
        config_ini.read(config_path)
        
        # DB Path
        db_path = os.path.join(base_dir, "ScalpelDatabase.sqlite")
        if 'Database' in config_ini and 'database_path' in config_ini['Database']:
            configured_path = config_ini['Database']['database_path']
            if not os.path.isabs(configured_path):
                db_path = os.path.normpath(os.path.join(base_dir, configured_path))
            else:
                db_path = configured_path
            
        # MPV Path
        mpv_path = "mpv.exe"
        if 'MPV' in config_ini and 'mpv_path' in config_ini['MPV']:
            mpv_path = config_ini['MPV']['mpv_path']
            
        return db_path, mpv_path

    def _build_ui(self):
        # --- Filter Frame ---
        filter_frame = ttk.LabelFrame(self.window, text="Filter Cases", padding=10)
