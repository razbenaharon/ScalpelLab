        # --- Case List ---
        list_frame = ttk.Frame(self.window, padding=10)
        list_frame.pack(fill=tk.BOTH, expand=True)
        
        # Removed "room" and "duration" columns
        columns = ("thumb", "date", "case", "cameras")
        self.tree = ttk.Treeview(list_frame, columns=columns, show="headings", selectmode="browse")
        
        self.tree.heading("thumb", text="Preview")
        self.tree.heading("date", text="Date")
        self.tree.heading("case", text="Case #")
        self.tree.heading("cameras", text="Cameras")
        
        self.tree.column("thumb", width=100, anchor="center")
        self.tree.column("date", width=120)
