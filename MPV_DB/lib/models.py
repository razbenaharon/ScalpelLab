@dataclass
class SyncAdjustment:
    """
    Tracks pending sync offset changes before database commit.

    Used for confirmation dialog display. This entity is IN-MEMORY ONLY and does
    not persist to database until user explicitly confirms.

    Lifecycle: Created when user clicks nudge button, cleared after DB save or cancel.
    """

    camera_name: str  # Camera identifier
    case_id: Tuple[str, int]  # (recording_date, case_no)
    old_offset: float  # Original offset from database
