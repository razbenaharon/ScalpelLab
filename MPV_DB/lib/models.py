    ipc_pipe_path: str  # Named pipe path for IPC
    current_timestamp: float = 0.0  # Current playback position (seconds)
    duration: float = 0.0  # Video duration (seconds)
    is_paused: bool = False  # Individual pause state

    # Sync State
    offset_seconds: float = 0.0  # Applied sync offset (from DB or user adjustment)
    offset_modified: bool = False  # True if offset changed since last DB save
    sync_delta: float = 0.0  # Calculated delta from reference camera (seconds)
    sync_status: str = "synced"  # "synced" | "out_of_sync"
    sync_status_text: str = "[OK] Synced"  # Display text

    # UI State
    is_selected: bool = False  # Currently selected in sync panel list
    is_reference: bool = False  # Designated as reference camera

    def __post_init__(self):
        """Validate attributes after initialization"""
        # Round offset to 1 decimal place (0.1s increments)
        self.offset_seconds = round(self.offset_seconds, 1)

        # Validate offset range (±300 seconds = ±5 minutes)
        if not (-300.0 <= self.offset_seconds <= 300.0):
            raise ValueError(
                f"Offset {self.offset_seconds}s outside allowed range (±300s)"
            )

        # Validate timestamp
        if self.current_timestamp < 0:
            raise ValueError(
                f"Timestamp cannot be negative: {self.current_timestamp}"
            )

    def update_sync_status(self, reference_timestamp: float, tolerance: float = 0.3):
        """
        Calculate sync status relative to reference camera.

        Args:
            reference_timestamp: Current timestamp of reference camera
            tolerance: Sync tolerance in seconds (default: 0.3s per REQ-1.1)

        Updates:
            - self.sync_delta: Delta from reference in seconds
            - self.sync_status: "synced" or "out_of_sync"
            - self.sync_status_text: Display string with delta
        """
        # Calculate delta (accounting for applied offset)
        self.sync_delta = (
            self.current_timestamp - reference_timestamp + self.offset_seconds
        )

        # Classify sync status
        if abs(self.sync_delta) <= tolerance:
            self.sync_status = "synced"
            self.sync_status_text = "[OK] Synced"
        else:
            self.sync_status = "out_of_sync"
            if self.sync_delta > 0:
                self.sync_status_text = f"+{self.sync_delta:.1f}s ahead"
            else:
                self.sync_status_text = f"{self.sync_delta:.1f}s behind"


